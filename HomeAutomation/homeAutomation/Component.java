package homeAutomation;

import java.util.ArrayList;

/**
 * Class responsible for managing statuses of components.
 * @author tblijlevens
 *
 */
public class Component {

  //  ATRIBUTES
  private String name = "";
  private boolean Status = false;

  /**
   * Creates a new component with given name and default status=false.
   * @param name the name of the component
   */
  // CONSTRUCTOR
  public Component(String name){
    this.name = name;
  }


  // METHODS
  public String getName(){
    return name;
  }
  public boolean getStatus(){
    return Status;
  }

  /**
   * Changes the status of this component to the given status
   * @param status the status this component should get.
   */
  public void changeStatus(boolean status){
    this.Status = status;
  }


}
